### Default Buttons

Button

```vue
<button class="btn">Button</button>
```

Disabled button

```vue
<button class="btn" disabled>Disabled button</button>
```

Form submit button

```vue
<input class="btn" type="submit" value="A form submit button" />
```

Link button

```vue
<a class="btn" href="#">Link button</a>
```

